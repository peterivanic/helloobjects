package kalkulacka;

public class Kalkulacka {

    public float plus(float cislo1,float cislo2 ){
        float pv = cislo1 + cislo2;
        return pv;
    }

    public float minus(float cislo1,float cislo2){
        float mv = cislo1 - cislo2;
        return mv;

    }

    public float krat(float cislo1 , float cislo2){
        float kv = cislo1 * cislo2;
        return kv;
    }

    public float delene(float cislo1, float cislo2){
        float dv = cislo1 / cislo2;
        return dv;
    }
}
